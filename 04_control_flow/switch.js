// switch statement

const month = 3

switch (month) {
    case 1:
        console.log("January");
        break;

        case 2:
        console.log("February");
        break;

        case 3:
        console.log("March");
        break;

    default:
        console.log("Invalid month");
        
        break;
}

const day = "monday"

switch (day) {
    case "sunday":
        console.log("Sunday");
        break;

        case "monday":
        console.log("Monday");
        break;

        case "tuesday":
        console.log("Tuesday");
        break;

    default:
        console.log("Invalid month");
        
        break;
}